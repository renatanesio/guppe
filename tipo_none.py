"""
Tipo None

O tipo de dado None em Python representa o tipo sem tipo, ou poderia ser conhecido
também como tipo vazio, mas falar que é um tipo sem tipo é mais apropriado

OBS: O tipo none é SEMPRE especificado com a primeira letra maiúscula

Quando utilizar?

- Podemos utilizar None quando queremos criar uma variável e inicializá-la com um tipo sem tipo,
antes de recebermos um valor final
"""

numeros = None

print(numeros)
print(type(numeros))

